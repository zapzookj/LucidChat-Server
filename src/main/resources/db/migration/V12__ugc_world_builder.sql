-- → Stage 4 바인딩에서 Character에 주입
ALTER TABLE character_creation_jobs ADD COLUMN IF NOT EXISTS requested_world_id     VARCHAR(50); -- WorldId enum
ALTER TABLE character_creation_jobs ADD COLUMN IF NOT EXISTS requested_ugc_world_id BIGINT;
